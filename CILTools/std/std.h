
/// Standard library functions

/// Print: Print a string, without a newline
void print(const char* str);

/// Println: Print a string with a newline
void println(const char* str);

/// PrintN: Print a string and a number, with a newline
void printn(const char* str, int n);

/// PrintS: Print a string and a string, with a newline
void prints(const char* str1, const char* str2);

/// Strlen: Get the length of a string
int strlen(const char* str);

/// Linein: Get a line of input from the user
char* linein();

/// Getchar: Get a single character from the user
char* getchar();